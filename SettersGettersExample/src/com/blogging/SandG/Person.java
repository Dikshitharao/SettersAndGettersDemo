package com.blogging.SandG;

public class Person {
	private String name;
	private int id;
	private String Adress;
	
	public String getName() {//getter for name
		return name;	
	}
	public void setName(String name) {//setter for name
		this.name = name;
	}
	public int getId() {//getter for id
		return id;
	}
	public void setId(int id) {//setter for id
		this.id = id;
	}
	public String getAdress() {//getter for address
		return Adress;
	}
	public void setAdress(String adress) {//setter for address
		Adress = adress;
	}
	
	
	
	
	
	

}
